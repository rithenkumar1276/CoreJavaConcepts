package com.evergent.CoreJava.Collections_task3;

public class Fruit  {
	private String fname;
	private int  price;
	private int Total;
	
	public void setFname(String fname) {
		this.fname = fname;
	}
	public String getFname() {
		return fname;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getPrice() {
		return price;
	}
	public void setTotal(int Total) {
		this.Total = Total;
	}
	public int getTotal() {
		return Total;
	}	
	
	
	
	
}
